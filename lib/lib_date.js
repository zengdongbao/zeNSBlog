// For convenience...
Date.prototype.format = function (mask, utc) {
    return Date.dateFormat(this, mask, utc);
};

Date.origParse = Date.parse;
Date.parse = function (date) {
    if (/\d{10}/.test(date)) {
        return parseInt(date) * 1000;
    }

    var timestamp = Date.origParse(date), minutesOffset = 0, struct;
    if (isNaN(timestamp) && /\+/.test(date)) {
        date = date.replace('+', 'UTC+');
        timestamp = Date.origParse(date);
    }

    return timestamp;
};